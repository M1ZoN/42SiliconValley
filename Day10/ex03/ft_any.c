int		ft_any(char **tab, int (*f)(char*))
{
	int		result;
	int		i;

	i = 0;
	result = 0;
	while (tab[i])
	{
		result = f(tab[i]);
		if (result == 1)
			return (1);
		i++;
	}
	return (0);
}
