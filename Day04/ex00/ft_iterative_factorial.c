int	ft_iterative_factorial(int nb)
{
	int	a;

	if (nb < 0 || nb >= 13)
	{
		a = 0;
	}
	else
	{
		a = 1;
		while (nb > 1)
		{
			a *= nb;
			nb--;
		}
	}
	return (a);
}
