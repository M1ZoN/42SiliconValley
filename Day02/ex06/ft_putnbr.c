void	ft_putchar(char	c);

void	ft_putnbr(int nb)
{
	int			b;
	long long	a;

	a = 0;
	b = 1;
	if (nb < 0)
	{
		b = -1;
	}
	while (nb > 0)
	{
		a = a * 10 + (nb % 10);
		nb /= 10;
	}
	a *= b;
	while (a > 0)
	{
		ft_putchar((a % 10) + '0');
		a /= 10;
	}
	if (nb == 0)
	{
		ft_putchar('0');
	}
}
